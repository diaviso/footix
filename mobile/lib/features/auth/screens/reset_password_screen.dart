import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:go_router/go_router.dart';
import '../../../core/theme/app_colors.dart';
import '../../../core/theme/app_design.dart';
import '../../../data/api/api_exception.dart';
import '../../../navigation/app_router.dart';
import '../../../providers/service_providers.dart';
import '../widgets/auth_background.dart';

class ResetPasswordScreen extends ConsumerStatefulWidget {
  final String token;
  const ResetPasswordScreen({super.key, required this.token});

  @override
  ConsumerState<ResetPasswordScreen> createState() =>
      _ResetPasswordScreenState();
}

class _ResetPasswordScreenState extends ConsumerState<ResetPasswordScreen> {
  final _formKey = GlobalKey<FormState>();
  final _passwordController = TextEditingController();
  final _confirmController = TextEditingController();
  bool _showPassword = false;
  bool _showConfirm = false;
  bool _isLoading = false;
  bool _isSuccess = false;

  @override
  void initState() {
    super.initState();
    if (widget.token.isEmpty) {
      WidgetsBinding.instance.addPostFrameCallback((_) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Le lien de réinitialisation est invalide ou a expiré'),
            backgroundColor: AppColors.error,
          ),
        );
        context.go(AppRoutes.forgotPassword);
      });
    }
  }

  @override
  void dispose() {
    _passwordController.dispose();
    _confirmController.dispose();
    super.dispose();
  }

  Future<void> _handleSubmit() async {
    if (!_formKey.currentState!.validate()) return;

    if (_passwordController.text != _confirmController.text) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Les mots de passe ne correspondent pas'),
          backgroundColor: AppColors.error,
        ),
      );
      return;
    }

    setState(() => _isLoading = true);
    try {
      final authService = ref.read(authServiceProvider);
      await authService.resetPassword(
        token: widget.token,
        password: _passwordController.text,
      );
      if (mounted) {
        setState(() => _isSuccess = true);
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Mot de passe réinitialisé avec succès'),
            backgroundColor: AppColors.success,
          ),
        );
      }
    } on ApiException catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(e.message),
            backgroundColor: AppColors.error,
          ),
        );
      }
    } catch (_) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('Une erreur est survenue'),
            backgroundColor: AppColors.error,
          ),
        );
      }
    } finally {
      if (mounted) setState(() => _isLoading = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    if (widget.token.isEmpty) {
      return AuthBackground(
        child: SafeArea(
          child: Center(
            child: Padding(
              padding: const EdgeInsets.all(24),
              child: AuthStaggeredItem(
                index: 0,
                child: AuthCard(
                  child: Padding(
                    padding: const EdgeInsets.all(32),
                    child: Column(mainAxisSize: MainAxisSize.min, children: [
                      Container(
                        width: 72, height: 72,
                        decoration: BoxDecoration(
                          color: AppColors.error.withValues(alpha: 0.2),
                          shape: BoxShape.circle,
                        ),
                        child: const Icon(Icons.cancel_rounded, size: 36, color: AppColors.error),
                      ),
                      const SizedBox(height: 24),
                      const Text('Lien invalide', style: TextStyle(fontSize: 22, fontWeight: FontWeight.bold, color: Colors.white)),
                      const SizedBox(height: 8),
                      Text('Le lien de réinitialisation est invalide ou a expiré.',
                          textAlign: TextAlign.center, style: TextStyle(fontSize: 14, color: Colors.white.withValues(alpha: 0.6))),
                      const SizedBox(height: 24),
                      _buildGradientButton('Demander un nouveau lien', () => context.go(AppRoutes.forgotPassword)),
                    ]),
                  ),
                ),
              ),
            ),
          ),
        ),
      );
    }

    if (_isSuccess) {
      return AuthBackground(
        child: SafeArea(
          child: Center(
            child: Padding(
              padding: const EdgeInsets.all(24),
              child: AuthStaggeredItem(
                index: 0,
                child: AuthCard(
                  child: Padding(
                    padding: const EdgeInsets.all(32),
                    child: Column(mainAxisSize: MainAxisSize.min, children: [
                      Container(
                        width: 72, height: 72,
                        decoration: BoxDecoration(
                          gradient: AppDesign.primaryGradient,
                          shape: BoxShape.circle,
                          boxShadow: AppDesign.glowShadow(AppColors.primary, blur: 16),
                        ),
                        child: const Icon(Icons.check_circle_rounded, size: 36, color: Colors.white),
                      ),
                      const SizedBox(height: 24),
                      const Text('Mot de passe réinitialisé !', style: TextStyle(fontSize: 22, fontWeight: FontWeight.bold, color: Colors.white)),
                      const SizedBox(height: 8),
                      Text('Votre mot de passe a été modifié avec succès. Vous pouvez maintenant vous connecter.',
                          textAlign: TextAlign.center, style: TextStyle(fontSize: 14, color: Colors.white.withValues(alpha: 0.6))),
                      const SizedBox(height: 24),
                      _buildGradientButton('Se connecter', () => context.go(AppRoutes.login)),
                    ]),
                  ),
                ),
              ),
            ),
          ),
        ),
      );
    }

    return AuthBackground(
      child: SafeArea(
        child: Center(
          child: SingleChildScrollView(
            padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 16),
            child: AuthStaggeredItem(
              index: 0,
              child: AuthCard(
                child: Padding(
                  padding: const EdgeInsets.all(24),
                  child: Column(children: [
                    // Icon
                    Container(
                      width: 64, height: 64,
                      decoration: BoxDecoration(
                        gradient: AppDesign.primaryGradient,
                        shape: BoxShape.circle,
                        boxShadow: AppDesign.glowShadow(AppColors.primary, blur: 14),
                      ),
                      child: const Icon(Icons.lock_rounded, size: 30, color: Colors.white),
                    ),
                    const SizedBox(height: 20),
                    const Text('Nouveau mot de passe', style: TextStyle(fontSize: 22, fontWeight: FontWeight.bold, color: Colors.white)),
                    const SizedBox(height: 8),
                    Text('Choisissez un nouveau mot de passe sécurisé pour votre compte.',
                        textAlign: TextAlign.center, style: TextStyle(fontSize: 13, color: Colors.white.withValues(alpha: 0.6))),
                    const SizedBox(height: 24),

                    // Form
                    Form(
                      key: _formKey,
                      child: Column(children: [
                        _buildPasswordField('Nouveau mot de passe', 'Minimum 6 caractères', _passwordController, _showPassword,
                            () => setState(() => _showPassword = !_showPassword),
                            validator: (v) {
                              if (v == null || v.isEmpty) return 'Veuillez entrer un mot de passe';
                              if (v.length < 6) return 'Minimum 6 caractères';
                              return null;
                            }),
                        const SizedBox(height: 16),
                        _buildPasswordField('Confirmer le mot de passe', 'Retapez votre mot de passe', _confirmController, _showConfirm,
                            () => setState(() => _showConfirm = !_showConfirm),
                            textInputAction: TextInputAction.done,
                            onSubmitted: (_) => _handleSubmit(),
                            validator: (v) {
                              if (v == null || v.isEmpty) return 'Veuillez confirmer le mot de passe';
                              if (v != _passwordController.text) return 'Les mots de passe ne correspondent pas';
                              return null;
                            }),
                        const SizedBox(height: 20),
                        _buildGradientButton(
                          _isLoading ? 'Réinitialisation...' : 'Réinitialiser le mot de passe',
                          _isLoading ? null : _handleSubmit,
                          isLoading: _isLoading,
                        ),
                      ]),
                    ),
                    const SizedBox(height: 16),

                    // Back to login
                    TextButton.icon(
                      onPressed: () => context.go(AppRoutes.login),
                      icon: const Icon(Icons.arrow_back_rounded, size: 16),
                      label: const Text('Retour à la connexion'),
                      style: TextButton.styleFrom(foregroundColor: Colors.white.withValues(alpha: 0.5)),
                    ),
                  ]),
                ),
              ),
            ),
          ),
        ),
      ),
    );
  }

  Widget _buildPasswordField(String label, String hint, TextEditingController controller, bool showPassword, VoidCallback toggleVisibility, {
    TextInputAction? textInputAction,
    void Function(String)? onSubmitted,
    String? Function(String?)? validator,
  }) {
    return Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
      Text(label, style: TextStyle(fontSize: 13, fontWeight: FontWeight.w600, color: Colors.white.withValues(alpha: 0.7))),
      const SizedBox(height: 6),
      TextFormField(
        controller: controller,
        obscureText: !showPassword,
        textInputAction: textInputAction ?? TextInputAction.next,
        onFieldSubmitted: onSubmitted,
        validator: validator,
        style: const TextStyle(color: Colors.white, fontSize: 15),
        decoration: InputDecoration(
          hintText: hint,
          hintStyle: TextStyle(color: Colors.white.withValues(alpha: 0.3)),
          prefixIcon: Icon(Icons.lock_outline_rounded, size: 20, color: Colors.white.withValues(alpha: 0.5)),
          suffixIcon: IconButton(
            icon: Icon(showPassword ? Icons.visibility_off_outlined : Icons.visibility_outlined, size: 20, color: Colors.white.withValues(alpha: 0.5)),
            onPressed: toggleVisibility,
          ),
          filled: true,
          fillColor: Colors.white.withValues(alpha: 0.08),
          contentPadding: const EdgeInsets.symmetric(horizontal: 16, vertical: 14),
          border: OutlineInputBorder(borderRadius: BorderRadius.circular(14), borderSide: BorderSide(color: Colors.white.withValues(alpha: 0.12))),
          enabledBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(14), borderSide: BorderSide(color: Colors.white.withValues(alpha: 0.12))),
          focusedBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(14), borderSide: const BorderSide(color: AppColors.accent, width: 1.5)),
          errorBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(14), borderSide: const BorderSide(color: AppColors.error)),
          errorStyle: const TextStyle(color: Color(0xFFFF8A80)),
        ),
      ),
    ]);
  }

  Widget _buildGradientButton(String text, VoidCallback? onPressed, {bool isLoading = false}) {
    return SizedBox(
      width: double.infinity, height: 52,
      child: DecoratedBox(
        decoration: BoxDecoration(
          gradient: onPressed != null ? AppDesign.primaryGradient : null,
          color: onPressed == null ? Colors.white.withValues(alpha: 0.1) : null,
          borderRadius: BorderRadius.circular(14),
          boxShadow: onPressed != null ? AppDesign.glowShadow(AppColors.primary, blur: 12) : null,
        ),
        child: ElevatedButton(
          onPressed: onPressed,
          style: ElevatedButton.styleFrom(
            backgroundColor: Colors.transparent,
            shadowColor: Colors.transparent,
            foregroundColor: Colors.white,
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(14)),
          ),
          child: isLoading
              ? const SizedBox(width: 20, height: 20, child: CircularProgressIndicator(strokeWidth: 2, color: Colors.white))
              : Text(text, style: const TextStyle(fontSize: 15, fontWeight: FontWeight.w600)),
        ),
      ),
    );
  }
}
